
function popUpWindowCoverage(type, coverage, content, scheme) {
	var URL = type + "?coverage=" + coverage + "&contentType=" + content + "&scheme=" + scheme;
	var windowName = "InsertCoverateMaster";
	var width = screen.availWidth;
	var height = screen.availHeight;
	var bwidth = window.innerWidth;
	var bwidth1 = document.body.clientWidth;
	if(bwidth <= 768) {
		var w = bwidth1;
		var h = 500;
	} else {
		var w = 640;
		var h = 500;
	}
	var features = "width=" + w + ",height=" + h + ",left=" + ((width - w - 10) * 0.5) + ",top=" + ((height - h - 30) * 0.5) + ",directories=no" + ",location=no" + ",menubar=no" + ",scrollbars=yes" + ",status=yes" + ",toolbar=no" + ",resizable=true";
	var strOpen = window.open(URL, windowName, features);
	strOpen.focus();
	return false;
}
