package com.maan.Home.displayBean;

public class CustomerListBean {
	private String customerID;

	private String customerName;

	private String emailID;

	private String contactNO;

	private String quoteNo;
	
	private String customerOpt;
	
	public String getContactNO() {
		return contactNO;
	}

	public void setContactNO(final String contactNO) {
		this.contactNO = contactNO;
	}

	public String getCustomerID() {
		return customerID;
	}

	public void setCustomerID(final String customerID) {
		this.customerID = customerID;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(final String customerName) {
		this.customerName = customerName;
	}

	public String getEmailID() {
		return emailID;
	}

	public void setEmailID(final String emailID) {
		this.emailID = emailID;
	}

	public String getQuoteNo() {
		return quoteNo;
	}

	public void setQuoteNo(final String quoteNo) {
		this.quoteNo = quoteNo;
	}

	public String getCustomerOpt() {
		return customerOpt;
	}

	public void setCustomerOpt(String customerOpt) {
		this.customerOpt = customerOpt;
	}
	
	
}
