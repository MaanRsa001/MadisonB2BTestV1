package com.maan.payment.airtel.model;

import com.google.gson.annotations.SerializedName;

public class TransactionData {
	@SerializedName("id")
	private String id;
	@SerializedName("status")
	private String status;
	@SerializedName("airtel_money_id")
	private String airtelmoneyid;
	@SerializedName("message")
	private String message;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAirtelmoneyid() {
		return airtelmoneyid;
	}
	public void setAirtelmoneyid(String airtelmoneyid) {
		this.airtelmoneyid = airtelmoneyid;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
		
}